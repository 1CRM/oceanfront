  result['id'] = id
  result['class'] = 'field-input field-text'
  return result
}

const loadContextAttrs = (attrs: Record<string, any>, state: FieldState) => {
  if (!attrs) return
  const result = {}
  for (const k in attrs) {
    if (k === 'config' || k === 'store' || k === 'value') continue
    if (hasOwn(mapStateAttrs, k)) {
      // FIXME may want to merge values, inputClass/Style can hold a list
      state[mapStateAttrs[k]] = attrs[k]
    } else {
      if (!state.inputAttrs) state.inputAttrs = {}
      state.inputAttrs[k] = attrs[k]
    }
  }
}

export default defineComponent({
  name: 'input-text',
  components: { FieldOuter },
  inheritAttrs: false,
  async setup(
    props: {
      config?: FieldConfig
      id?: string
      store?: StoreRef
      value?: any
      variant?: string
      [key: string]: any
    },
    ctx: SetupContext
  ) {
    const config = props.config || {}
    const elt = ref<HTMLInputElement>()
    const other = ref<HTMLElement>()
    const focused = ref(false)
    const id =
      props.id || config.id || 'input-' + Math.round(Math.random() * 1000) // FIXME
    const initValue = props.value === undefined ? config.value : props.value
    const store = props.store || storeRef(initValue)
    const value: Ref<any> = computed({
      get: () => {
        // FIXME format stored value
        // computed() can be generated by the formatter
        store.value
      },
      set: (val: any) => {
        // FIXME unformat value
        store.value = val
      }
    })
    const inputValue = ref(initValue)
    const handlers = {
      blur: (evt: FocusEvent) => {
        focused.value = false
      },
      change: (evt: Event) => {
        inputValue.value = elt.value.value
        store.value = elt.value.value
      },
      input: (evt: InputEvent) => {
        inputValue.value = elt.value.value
      },
      focus: (evt: FocusEvent) => {
        focused.value = true
      }
    }
    const blank = computed(() => {
      const val = inputValue.value
      console.log(val)
      return val === undefined || val === null || val === ''
    })
    const fieldAttrs = computed(() => ({
      blank: blank.value,
      config,
      focused: focused.value,
      inputId: id,
      store,
      variant: props.variant
    }))
    return {
      attrs: computed(() => ({
        id,
        class: 'field-input field-text'
      })),
      blank,
      elt,
      fieldAttrs,
      focused,
      handlers,
      id,
      inputValue,
      other,
      store,
      value
    }
  }
})
</script>
