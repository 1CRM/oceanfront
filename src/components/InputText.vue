<template>
  <div ref="other"></div>
  <field-outer v-bind="fieldAttrs">
    <input ref="elt" v-bind="attrs" v-on="handlers" :value="inputValue" />
  </field-outer>
</template>

<script lang="ts">
import {
  ref,
  defineComponent,
  SetupContext,
  reactive,
  computed,
  Ref
} from 'vue'
import { FieldConfig, FieldState, fieldState } from '../lib/field'
import FieldOuter from './FieldOuter.vue'
import { StoreRef, storeRef } from '../lib/store'
import { hasOwn } from '../lib/util'

const copyAttrs = new Set([
  'autocomplete',
  'placeholder',
  'size',
  'tabIndex',
  'value'
])

const mapStateAttrs = {
  id: 'inputId',
  class: 'inputClass',
  maxlength: 'maxLength',
  readonly: 'readOnly',
  required: 'required',
  style: 'inputStyle',
  tabindex: 'tabIndex',
  type: 'inputType',
  variant: 'inputVariant'
}

// types that can be inherited from state.type
// setting state.inputType allows any value
const allowInputTypes = new Set([
  'date',
  'datetime-local',
  'email',
  'month',
  'number',
  'password',
  'tel',
  'time',
  'week',
  'url'
])

const inputTypeFrom = (type?: string) => {
  if (type && allowInputTypes.has(type)) return type
  return 'text'
}

const variantClasses: Record<string, string> = {
  default: 'input-text',
  plain: ''
}

const makeAttrs = (
  config: FieldConfig,
  id: string,
  attrs: Record<string, any>
) => {
  const result: Record<string, any> = {}
  /*for (const k of copyAttrs) {
