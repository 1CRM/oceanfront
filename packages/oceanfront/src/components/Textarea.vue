<template>
  <of-field-outer v-bind="fieldAttrs">
    <textarea
      ref="elt"
      v-bind="attrs"
      v-on="handlers"
      :value="inputValue"
    ></textarea>
  </of-field-outer>
</template>

<script lang="ts">
import { ref, defineComponent, SetupContext, computed, Ref, watch } from 'vue'
import OfFieldOuter from './FieldOuter.vue'

